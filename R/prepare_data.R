sites <- read_csv2("data_raw_sites.csv", col_names = TRUE,
                   col_types =
                    cols(
                      .default = col_double(),
                      id = col_factor(),
                      treatment = col_factor(),
                      plotHeight = col_factor(),
                      bankType = col_factor(),
                      structureType = col_character(),
